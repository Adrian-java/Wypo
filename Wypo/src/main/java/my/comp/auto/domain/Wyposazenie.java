package my.comp.auto.domain;



public class Wyposazenie {
	
	private int idWyposazenie;
	private String typ;
	private String klimatyzacja;
	private String skrzyniaBiegow;
	private int lDrzwi;
	private String fotele;
	private String szyby;
	
	public int getIdWyposazenie() {
		return idWyposazenie;
	}
	public void setIdWyposazenie(int idWyposazenie) {
		this.idWyposazenie = idWyposazenie;
	}
	public String getTyp() {
		return typ;
	}
	public void setTyp(String typ) {
		this.typ = typ;
	}
	public String getKlimatyzacja() {
		return klimatyzacja;
	}
	public void setKlimatyzacja(String klimatyzacja) {
		this.klimatyzacja = klimatyzacja;
	}
	public String getSkrzyniaBiegow() {
		return skrzyniaBiegow;
	}
	public void setSkrzyniaBiegow(String skrzyniaBiegow) {
		this.skrzyniaBiegow = skrzyniaBiegow;
	}
	public int getlDrzwi() {
		return lDrzwi;
	}
	public void setlDrzwi(int lDrzwi) {
		this.lDrzwi = lDrzwi;
	}
	public String getFotele() {
		return fotele;
	}
	public void setFotele(String fotele) {
		this.fotele = fotele;
	}
	public String getSzyby() {
		return szyby;
	}
	public void setSzyby(String szyby) {
		this.szyby = szyby;
	}
	
	
}
