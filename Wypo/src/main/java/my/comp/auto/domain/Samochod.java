package my.comp.auto.domain;



public class Samochod {

	private String vin;
	private int cena;
	private int dostepny;
	private int idMarmod;
	private int idPunkt;
	
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}
	public int getCena() {
		return cena;
	}
	public void setCena(int cena) {
		this.cena = cena;
	}
	public int getDostepny() {
		return dostepny;
	}
	public void setDostepny(int dostepny) {
		this.dostepny = dostepny;
	}
	public int getIdMarmod() {
		return idMarmod;
	}
	public void setIdMarmod(int idMarmod) {
		this.idMarmod = idMarmod;
	}
	public int getIdPunkt() {
		return idPunkt;
	}
	public void setIdPunkt(int idPunkt) {
		this.idPunkt = idPunkt;
	}
	
	
}
